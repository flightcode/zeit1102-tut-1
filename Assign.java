public class Assign
{
    int integer;
    float floating;
    double doubleFloat;
    long longer;
    char character;
    String string;
    public Assign()
    {
        integer = 2;
        System.out.println("Int: " + integer);
        floating = 2;
        System.out.println("Float: " + floating);
        doubleFloat = 2;
        System.out.println("Double: " + doubleFloat);
        longer = 2;
        System.out.println("Long: " + longer);
        character = '2';
        System.out.println("Char: " + character);
        string = "2";
        System.out.println("String: " + string);
    }
}
